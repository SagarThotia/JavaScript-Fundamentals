/***********************************String.prototype.toLocaleUpperCase()*********************************/
/*
    The toLocaleUpperCase() method of String values returns this string converted to upper case, according to any locale-specific case mappings

    Syntax:
    toLocaleUpperCase()
    toLocaleUpperCase(locales)
*/

const city = 'mumbai';

console.log(`EN-US: $city.toLocaleUpperCase('en-US')`); // Expected output: "MUMBAI"

console.log(`IN: city.toLocaleUpperCase('IN')`); // Expected output: "MUMBAI"



